function [M1,M2] = MomentsRaw2(l1,l2)


    % first raw moment
    M11 = @(x0,D1,D2) (0.1e1/D1*l1+0.1e1/D1*l2)*x0-0.1e1/D1*x0^2/0.2e1;
    M12 = @(x0,D1,D2) -0.1e1/D2*l1^2/0.2e1-l1/D2*l2+0.1e1/D1*l1^2/0.2e1+l1/D1*l2+(0.1e1/D2*l1+0.1e1/D2*l2)*x0-0.1e1/D2*x0^2/0.2e1;
    M1 = @(x0,p1,p2) (x0<l1)*M11(x0,p1,p2)+(x0>=l1 && x0 <= l1+l2)*M12(x0,p1,p2);

    % second raw moment
    M21 = @(x0,D1,D2) (0.2e1/0.3e1/D1/D2*l2^3+0.2e1/0.3e1/D1^2*l1^3+0.2e1/D1^2*l1^2*l2+0.2e1/D1^2*l1*l2^2)*x0+(-0.1e1/D1^2*l1/0.3e1-0.1e1/D1^2*l2/0.3e1)*x0^3+0.1e1/D1^2*x0^4/0.12e2;
    M22 = @(x0,D1,D2) l1^4/D2^2/0.12e2+l1^3/D2^2*l2/0.3e1-0.2e1/0.3e1*l1/D2^2*l2^3-l1^4/D2/D1/0.2e1-0.2e1*l1^3/D2/D1*l2-0.2e1*l1^2/D2/D1*l2^2+0.2e1/0.3e1*l1/D2/D1*l2^3+0.5e1/0.12e2*l1^4/D1^2+0.5e1/0.3e1*l1^3/D1^2*l2+0.2e1*l1^2/D1^2*l2^2+(-0.1e1/D2^2*l1^3/0.3e1-0.1e1/D2^2*l1^2*l2+0.2e1/0.3e1/D2^2*l2^3+0.1e1/D2/D1*l1^3+0.3e1/D2/D1*l1^2*l2+0.2e1/D2/D1*l1*l2^2)*x0+(0.1e1/D2^2*l1^2/0.2e1+0.1e1/D2^2*l1*l2-0.1e1/D2*l1^2/D1/0.2e1-0.1e1/D2*l1/D1*l2)*x0^2+(-0.1e1/D2^2*l1/0.3e1-0.1e1/D2^2*l2/0.3e1)*x0^3+0.1e1/D2^2*x0^4/0.12e2;
    M2 = @(x0,p1,p2) (x0<l1)*M21(x0,p1,p2)+(x0>=l1 && x0 <= l1+l2)*M22(x0,p1,p2);

end

